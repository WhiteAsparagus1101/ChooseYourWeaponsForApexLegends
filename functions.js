const includeSupplyDrop = true;


function choose() {
    let results = [];

    for (let i = 0; i < 2; i++) {
        let result = {};
        let weapon = weapons[Math.floor(Math.random() * weapons.length)];
        if (!includeSupplyDrop) {
            while (weapon["supply drop"] == true) {
                weapon = weapons[Math.floor(Math.random() * weapons.length)];
            }
        }
        result["name"] = weapon["name"];

        let attatch = {};
        for (const [key, value] of Object.entries(weapon["attatchment"])) {
            attatch[key] = value[Math.floor(Math.random() * value.length)];
        }
        result["attatchment"] = attatch;

        results.push(result);
    }

    // console.log(results);
    let display = "";

    for (let i = 0; i < 2; i++) {
        let res = results[i];
        display += `<h2>Weapon${i+1}</h2><table><thead><tr><th>${res["name"]}</th></tr></thead><tbody>`;
        for (const [key, value] of Object.entries(res["attatchment"])) {
            display += `<tr><td>${key}</td><td>${value}</td></tr>`
        }
        display += "</tbody></table>"
    }

    let d1 = document.getElementById("results");
    d1.innerHTML = display;
}

window.addEventListener("load", () => {
    let btn = document.getElementById("b1");
    btn.addEventListener("click", choose, false);
})